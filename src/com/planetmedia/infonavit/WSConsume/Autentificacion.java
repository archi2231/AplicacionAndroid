package com.planetmedia.infonavit.WSConsume;

public class Autentificacion {
	
	private String code;
	private String message;
	private String givenName;
	private String infonavitActive;
	private String infonavitCCnumber;
	private String nss;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getGivenName() {
		return givenName;
	}
	public void setGivenName(String givenName) {
		this.givenName = givenName;
	}
	public String getInfonavitActive() {
		return infonavitActive;
	}
	public void setInfonavitActive(String infonavitActive) {
		this.infonavitActive = infonavitActive;
	}
	public String getInfonavitCCnumber() {
		return infonavitCCnumber;
	}
	public void setInfonavitCCnumber(String infonavitCCnumber) {
		this.infonavitCCnumber = infonavitCCnumber;
	}
	public String getNss() {
		return nss;
	}
	public void setNss(String nss) {
		this.nss = nss;
	}

}
