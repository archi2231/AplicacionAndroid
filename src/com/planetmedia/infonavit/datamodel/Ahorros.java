package com.planetmedia.infonavit.datamodel;



public class Ahorros {
	
	private String idAhorros;
	private String descripcion;
	private String fechaDescarga;
	private String idUsuario;
	
	public String getIdAhorros() {
		return idAhorros;
	}
	public void setIdAhorros(String idAhorros) {
		this.idAhorros = idAhorros;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getFechaDescarga() {
		return fechaDescarga;
	}
	public void setFechaDescarga(String fechaDescarga) {
		this.fechaDescarga = fechaDescarga;
	}
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	
    
}